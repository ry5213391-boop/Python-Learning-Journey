#\n - new line or next line
#\t- tab or space
#\\- backslash
#\*- insets a single quote inside a single quoted string

#\n
print("Hello everyone.\nHow are you?")

#\t
print("Roshan\tYadav")

#\\
print("new\\old")
print("20\\01\\2005")

#\'
print('This is Python\'s class.')

#\"
print("He says,\" We are doing very well\".")